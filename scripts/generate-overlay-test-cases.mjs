import { readFile, readdir, stat, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { format, resolveConfig } from "prettier";

const COMMENT_SUFFIX = "-comments.json";
const PREFERRED_DEFAULT_ID = "sm6240144";
const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDirectory, "..");
const fixtureDirectory = path.join(projectRoot, "overlay-tests", "fixtures");
const outputFile = path.join(projectRoot, "overlay-tests", "src", "video-cases.generated.ts");

const isFile = async (filePath) => {
  try {
    return (await stat(filePath)).isFile();
  } catch (error) {
    if (error?.code === "ENOENT") {
      return false;
    }
    throw error;
  }
};

const readLabel = async (commentFile, fallback) => {
  const parsed = JSON.parse(await readFile(commentFile, "utf8"));
  return typeof parsed.label === "string" && parsed.label.trim().length > 0
    ? parsed.label
    : fallback;
};

const discoverCases = async () => {
  const entries = await readdir(fixtureDirectory, { withFileTypes: true });
  const commentFiles = entries
    .filter((entry) => entry.isFile() && entry.name.endsWith(COMMENT_SUFFIX))
    .map((entry) => entry.name)
    .sort((left, right) => left.localeCompare(right, "en"));

  const cases = [];
  for (const commentFileName of commentFiles) {
    const id = commentFileName.slice(0, -COMMENT_SUFFIX.length);
    const videoFileName = `${id}.mp4`;
    const videoFile = path.join(fixtureDirectory, videoFileName);
    if (!(await isFile(videoFile))) {
      throw new Error(`${commentFileName} に対応する ${videoFileName} がありません。`);
    }

    cases.push({
      id,
      label: await readLabel(path.join(fixtureDirectory, commentFileName), id),
      comments: `./fixtures/${commentFileName}`,
      video: `./fixtures/${videoFileName}`,
    });
  }

  if (cases.length === 0) {
    throw new Error("overlay-tests/fixtures に *-comments.json がありません。");
  }

  return cases;
};

const renderCases = (cases) => {
  const defaultId = cases.some(({ id }) => id === PREFERRED_DEFAULT_ID)
    ? PREFERRED_DEFAULT_ID
    : cases[0].id;
  const lines = [
    "// This file is generated by scripts/generate-overlay-test-cases.mjs.",
    "// Add a <case-id>-comments.json / <case-id>.mp4 pair under overlay-tests/fixtures, then rebuild.",
    "",
    "export const VIDEO_CASES = {",
  ];

  for (const testCase of cases) {
    lines.push(`  ${JSON.stringify(testCase.id)}: {`);
    lines.push(`    label: ${JSON.stringify(testCase.label)},`);
    lines.push(`    comments: ${JSON.stringify(testCase.comments)},`);
    lines.push(`    video: ${JSON.stringify(testCase.video)},`);
    lines.push("  },");
  }

  lines.push("} as const;");
  lines.push("");
  lines.push(`export const DEFAULT_VIDEO_CASE_ID = ${JSON.stringify(defaultId)} as const;`);
  lines.push("export const DEFAULT_COMMENT_DATA_SOURCES = [");
  lines.push("  VIDEO_CASES[DEFAULT_VIDEO_CASE_ID].comments,");
  lines.push("] as const;");
  lines.push("");
  lines.push("export type VideoCaseId = keyof typeof VIDEO_CASES;");
  lines.push("");
  return lines.join("\n");
};

const cases = await discoverCases();
const prettierOptions = (await resolveConfig(outputFile)) ?? {};
await writeFile(
  outputFile,
  await format(renderCases(cases), { ...prettierOptions, filepath: outputFile }),
  "utf8",
);
console.log(`overlay test cases: ${cases.length} -> ${path.relative(projectRoot, outputFile)}`);
